package org.technbolts.util

object CLI {
  object Color {
    val White = "\033[0m" // white (normal)
    val Black = "\033[30m" // black
    val Red = "\033[31m" // red
    val Green = "\033[32m" // green
    val Orange = "\033[33m" // orange
    val Blue = "\033[34m" // blue
    val Purple = "\033[35m" // purple
    val Cyan = "\033[36m" // cyan
    val Gray = "\033[37m" // gray
  }
}